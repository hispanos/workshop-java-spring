package com.workshop.workshopmaven.reader.model;

public class XlsxModel {
    private Integer date;
    private String injuryLocation;
    private String gender;
    private String ageGroup;
    private String incidentType;
    private Integer daysLost;
    private String plant;
    private String reportType;
    private String shift;
    private String department;
    private Integer incidentCost;
    private String wkDay;
    private Integer month;
    private Integer year;

    public XlsxModel() {
    }

    public Integer getDate() {
        return date;
    }

    public void setDate(Integer date) {
        this.date = date;
    }

    public String getInjuryLocation() {
        return injuryLocation;
    }

    public void setInjuryLocation(String injuryLocation) {
        this.injuryLocation = injuryLocation;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAgeGroup() {
        return ageGroup;
    }

    public void setAgeGroup(String ageGroup) {
        this.ageGroup = ageGroup;
    }

    public String getIncidentType() {
        return incidentType;
    }

    public void setIncidentType(String incidentType) {
        this.incidentType = incidentType;
    }

    public Integer getDaysLost() {
        return daysLost;
    }

    public void setDaysLost(Integer daysLost) {
        this.daysLost = daysLost;
    }

    public String getPlant() {
        return plant;
    }

    public void setPlant(String plant) {
        this.plant = plant;
    }

    public String getReportType() {
        return reportType;
    }

    public void setReportType(String reportType) {
        this.reportType = reportType;
    }

    public String getShift() {
        return shift;
    }

    public void setShift(String shift) {
        this.shift = shift;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public Integer getIncidentCost() {
        return incidentCost;
    }

    public void setIncidentCost(Integer incidentCost) {
        this.incidentCost = incidentCost;
    }

    public String getWkDay() {
        return wkDay;
    }

    public void setWkDay(String wkDay) {
        this.wkDay = wkDay;
    }

    public Integer getMonth() {
        return month;
    }

    public void setMonth(Integer month) {
        this.month = month;
    }

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

}
